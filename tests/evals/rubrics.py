"""Jovaltus skill evaluation rubrics.

Precise, banded scoring rubrics for each of the three Jovaltus tasks.
Every dimension uses discrete bands (0/3/5/7/10) with concrete criteria
so the LLM judge produces reproducible scores.

Design
------
- **Universal dimensions** (scored for both bare and jovaltus profiles):
  functional completeness, code quality, testing, runnability.
- **Jovaltus-only dimensions** (scored only for jovaltus-agent profile):
  pipeline adherence, phase discipline, token efficiency.

Weights are proportional: universal dimensions sum to 0.70,
jovaltus-only dimensions sum to 0.30.
"""

from __future__ import annotations

from fabricium.evals import RubricDimension, RubricSpec, ScoringBand

# ═════════════════════════════════════════════════════════════════════
# Reusable scoring band templates
# ═════════════════════════════════════════════════════════════════════

BAND_ALL_5 = [
    ScoringBand(10, "All 5 requirements met with no significant issues."),
    ScoringBand(7, "4/5 met, or all present but one has a clear bug."),
    ScoringBand(5, "3/5 met, or all present but ≥2 have clear bugs."),
    ScoringBand(3, "1–2/5 met. Basic skeleton exists."),
    ScoringBand(0, "No evidence of attempt, or code is non-functional."),
]

BAND_ALL_4 = [
    ScoringBand(10, "All 4 requirements met."),
    ScoringBand(7, "3/4 met."),
    ScoringBand(5, "2/4 met."),
    ScoringBand(3, "1/4 met."),
    ScoringBand(0, "No evidence."),
]

BAND_ALL_3 = [
    ScoringBand(10, "All 3 requirements met."),
    ScoringBand(7, "2/3 met."),
    ScoringBand(5, "1/3 met."),
    ScoringBand(0, "No evidence."),
]

BAND_BINARY = [
    ScoringBand(10, "Passed (exit code 0)."),
    ScoringBand(0, "Failed (non-zero exit code)."),
]

BAND_FOUR_PHASE = [
    ScoringBand(10, "All four phases clearly observable."),
    ScoringBand(7, "Three of four phases observable."),
    ScoringBand(5, "Two phases observable."),
    ScoringBand(3, "Only Implement phase observable."),
    ScoringBand(0, "No pipeline phases — single-shot output."),
]

# ═════════════════════════════════════════════════════════════════════
# Universal dimensions (scored for both bare and jovaltus)
# ═════════════════════════════════════════════════════════════════════

DIM_FUNCTIONAL = RubricDimension(
    id="functional_completeness",
    label="Functional Completeness",
    weight=0.25,
    description="Are all required features present and working correctly?",
    scoring_bands=[
        ScoringBand(
            10,
            "All required features present, working, and correctly connected.",
        ),
        ScoringBand(
            7,
            "Most features present (80%+). One missing or has a clear bug.",
        ),
        ScoringBand(
            5,
            "Roughly half present (50–70%). Core exists but gaps remain.",
        ),
        ScoringBand(
            3,
            "Only basic scaffolding (1–2 features). Major gaps.",
        ),
        ScoringBand(0, "No functional implementation."),
    ],
    evidence_hints=[
        "Check workspace file tree for expected source files.",
        "Run build/test commands.",
        "Examine code for feature-specific implementations.",
    ],
)

DIM_CODE_QUALITY = RubricDimension(
    id="code_quality",
    label="Code Quality",
    weight=0.20,
    description="Structure, error handling, type safety, maintainability.",
    scoring_bands=[
        ScoringBand(
            10,
            "Clear module separation, consistent type annotations, "
            "proper error handling throughout, clean structure.",
        ),
        ScoringBand(
            7,
            "Good structure with minor gaps: some missing types, "
            "incomplete error handling in a few places.",
        ),
        ScoringBand(
            5,
            "Adequate but noticeable gaps: most code in one file, "
            "minimal error handling, sparse type annotations.",
        ),
        ScoringBand(
            3,
            "Poor structure: single-file, no error handling, no types.",
        ),
        ScoringBand(0, "Unreadable or autogenerated boilerplate only."),
    ],
    evidence_hints=[
        "Check directory structure (modules, packages).",
        "Look for type annotations on function signatures.",
        "Check for error handling (try/except, HTTPException, etc.).",
    ],
)

DIM_TESTING = RubricDimension(
    id="testing",
    label="Testing Quality",
    weight=0.15,
    description="Are there tests? Do they pass? Do they cover core scenarios?",
    scoring_bands=[
        ScoringBand(
            10,
            "Tests for all endpoints/components, happy-path + error cases, "
            "use test fixtures, all pass.",
        ),
        ScoringBand(
            7,
            "Tests cover most components. One or two error scenarios missing. "
            "All pass.",
        ),
        ScoringBand(
            5,
            "Tests exist but spotty: 1–2 components, weak or missing assertions.",
        ),
        ScoringBand(
            3,
            "Test files exist but fail or have no real assertions.",
        ),
        ScoringBand(0, "No test files found."),
    ],
    evidence_hints=[
        "Check for test files (test_*.py, *.test.ts, __tests__/).",
        "Run test command, check exit code.",
        "Read test assertions — do they verify real behaviour?",
    ],
)

DIM_RUNNABILITY = RubricDimension(
    id="runnability",
    label="Runnability",
    weight=0.10,
    description="Can the project be built and run with standard commands?",
    scoring_bands=[
        ScoringBand(
            10,
            "All dependency files complete. Build/run succeeds first try.",
        ),
        ScoringBand(
            7,
            "Mostly complete, missing one package. Builds after minor fix.",
        ),
        ScoringBand(
            5,
            "Dependency file incomplete (missing key packages). Build fails.",
        ),
        ScoringBand(0, "No dependency file or cannot be built at all."),
    ],
    evidence_hints=[
        "Check pyproject.toml / package.json / requirements.txt.",
        "Run install + build commands.",
        "Check if imported packages are declared as dependencies.",
    ],
)

# ═════════════════════════════════════════════════════════════════════
# Jovaltus-specific dimensions (scored only for jovaltus profile)
# ═════════════════════════════════════════════════════════════════════

DIM_PIPELINE = RubricDimension(
    id="pipeline_adherence",
    label="Pipeline Adherence",
    weight=0.15,
    description="Followed Plan→Implement→Verify→Simplify workflow?",
    universal=False,
    scoring_bands=BAND_FOUR_PHASE,
    evidence_hints=[
        "Check agent trace for clarify tool calls (Plan phase).",
        "Count delegate_task or jovaltus_* tool invocations.",
        "Check git log for multiple commits with descriptive messages.",
        "Look for test-failure → fix → retest cycles (Verify phase).",
    ],
)

DIM_PHASE_DISCIPLINE = RubricDimension(
    id="phase_discipline",
    label="Phase Discipline",
    weight=0.10,
    description="Correct phase order without skipping steps?",
    universal=False,
    scoring_bands=[
        ScoringBand(
            10,
            "Strict order: clarify before code, verify after implement, "
            "simplify after verify. No premature file writing.",
        ),
        ScoringBand(
            7,
            "Correct order overall, one fuzzy boundary "
            "(e.g. started implementing during Plan).",
        ),
        ScoringBand(
            5,
            "Two order violations. For example: no clarify, verify skipped.",
        ),
        ScoringBand(
            3,
            "Phases out of order or indistinguishable. "
            "Jumps straight to implementation.",
        ),
        ScoringBand(
            0,
            "No phase discipline — everything in one burst.",
        ),
    ],
    evidence_hints=[
        "Check tool call chronology — clarify before file writes?",
        "Check if test execution appears between implement and simplify.",
        "Look for stage validation errors in the trace.",
    ],
)

DIM_TOKEN_EFFICIENCY = RubricDimension(
    id="token_efficiency",
    label="Token Efficiency",
    weight=0.05,
    description="Efficient token use without waste?",
    universal=False,
    scoring_bands=[
        ScoringBand(
            10,
            "No repeated reads of unchanged files, concise output, no loops.",
        ),
        ScoringBand(
            7,
            "Mostly efficient with minor waste (one or two unnecessary re-reads).",
        ),
        ScoringBand(
            5,
            "Noticeable waste: repeated file reads, verbose explanations.",
        ),
        ScoringBand(
            0,
            "Significant waste: 3+ re-reads of same file, "
            "looping, excessive verbosity.",
        ),
    ],
    evidence_hints=[
        "Check for duplicate read_file calls on the same path.",
        "Look for repetitive explanations or boilerplate.",
        "Check total token usage vs output size.",
    ],
)

